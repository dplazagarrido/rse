  <h1 class="display-3"> Liceo Emilia Toro de Balmaceda</h1></h1>
  <hr class="my-4">
  <p>El 17 de abril de 1906, siendo presidente de Chile Germán Riesco,  se efectúa  la primera sesión  de  la  Junta  de Vigilancia  que  daría  origen a  la  Escuela  Profesional  de Niñas Nº 2, a la que asistieron:  Juan Enrique Concha (alcalde de Santiago),  Eduardo Charme (Senador y ex Ministro de OO.PP.),  Antonio Huneeus (Ministro de Justicia e Instrucción Pública), Enrique Villegas (Empresario Salitrero y Ministro de Industria y Obras Públicas), y Ana Valck (futura Directora).

 

Se fijó el 7 de julio de 1906 como fecha de la fundación,  ubicada en la esquina nororiente de la calle Compañía de Jesús, con el número 2951. Se arrienda, además la casa colindante por calle Maipú con el número 445,  la primera Directora Ana Valck quedó autorizada para”elevar una propuesta”  que permitiera hacer mejoras a la propiedad y adquirir mobiliario.

 

Se recibe niñas  con sexto grado primario o básico, las que podían optar por las siguientes especialidades: modas, lencería, sombrerería, bordado, contabilidad, tejidos, guantes, corsetería,  camisería, flores de género.

 

En enero de 1912 se traslada al inmueble de  Matucana 403 que había sido anteriormente la Escuela de Minería de Santiago,  fundada en 1889 bajo el gobierno de José Manuel Balmaceda.  Estos terrenos habían pertenecido  en tiempos  de la Colonia al padre del ministro Diego Portales Palazuelos,  teniendo como límites la actual Avda. Portales, Sn. Pablo, Maipú y Gral. Velásquez.

 

Este edificio de Matucana 403 comienza a construirse en 1885, al mismo tiempo que lo hacía el que está ubicado en Compañía con Chacabuco que cobijó a la Escuela Normal de Preceptoras  o Normal Femenina Nº 1 (Gabriela Mistral se tituló ahí de profesora primaria en 1910).

 

Las alumnas que se incorporaban al Liceo  con enseñanza primaria o básica completa, es decir, 6 años, se titulaban al final del proceso como jefas de taller; y si  se incorporaban con  sus estudios  incompletos, egresaban como operarias especializadas.

 

La Escuela Técnica Nº 2 pasó a depender desde su fundación del Ministerio de Industria y Obras Públicas, al igual que todas las escuelas técnicas del país, siendo supervisada en sus planes de estudio por una Junta de Vigilancia que  dependía de la Escuela Profesional Superior de Santiago.

 

En 1915 se instala el alumbrado  eléctrico en la calle Matucana. Los tranvías  eran, aún, tirados por caballos, pero un año después se integran a la modernidad  usando la fuerza eléctrica.

 

Para el aniversario Nº 20 de la Técnica Nº 2, el 7 de julio de 1926, con la presencia del Ministro de Educación,  y  los hijos de Emilia Toro y del ex Presidente Balmaceda, se procede a bautizar al Colegio como Escuela Profesional de Niñas Emilia Toro de Balmaceda.

 

En 1927 el  Liceo deja el vínculo con el Ministerio de Obras Públicas y pasa,  a depender de la Dirección General de Enseñanza Industrial. Precisamente, de esa Dirección General se emite el decreto que contrata a partir  de agosto de 1927,  como profesora de dibujo  la que se convertiría posteriormente en la insigne escultora Laura Rodig, futura Premio Nacional de Arte.

 

  Por estos años se deja sentir con fuerza  la crisis producida por la baja de las exportaciones de salitre y el quiebre de la economía capitalista en 1929. El autoritarismo del presidente Carlos Ibáñez  hizo aumentar la efervescencia  social y política en el país. Bajo estas circunstancias la matrícula de la Escuela Profesional de Niñas Emilia Toro de Balmaceda disminuyó en un tercio,  lo que sólo se vino a recuperar parcialmente en la década de 1940, además, desaparecen los tranvías por la calle Matucana dando paso a las góndolas y microbuses.

 

El 14 de diciembre de 1935,  el Presidente Arturo Alessandri Palma acompañado del Ministro de Educación visitó nuestro colegio.

 

En agosto de 1942 por decreto N 6/4817, se crea la Dirección de Enseñanza Profesional de la que pasan a depender todas las escuelas técnicas femeninas.

 

En 1945, el Liceo acoge a algunas niñas en régimen de internado, para tal propósito  contaba con una propiedad, ahora inexistente, en Chacabuco Nº 559.  Cuando el Ministerio  vende dicha propiedad, el régimen de internado se mantiene hasta 1964 con literas instaladas en el gimnasio.  En  1950 se arrendó una casa en el N° 510 de Matucana donde se encuentra ahora el edificio de salida del metro Quinta  Normal. Dicha casa tuvo como propósito instalar un taller industrial de tejidos y para el medio pupilaje de las alumnas.

 

En julio de 1946, en una sesión del consejo de profesores de ese año  se decide ayudar a las alumnas indigentes obsequiándoles  “algunas piezas de ropa interior”,  “en forma reservada”. Con esto “se aprobó la idea de ayudar a los niños desamparados que dejó la guerra europea con dinero recolectado por las niñas”.  Otro hecho interesante, es que en 1947 figura contratada como profesora de flores artificiales la hermana de Pablo Neruda,  Laura Reyes.

 

                     Durante los años 1968 y 1969, el Liceo fue   conocido como Escuela de Servicios y Técnicas Especializadas. Entre los años 1970 y 1971, nuestro nombre cambia a   Escuela Superior Nº2   Emilia Toro de Balmaceda.

 

El 23  de septiembre de 1978, el Centro Educativo,  pasa a llamarse Liceo Técnico A  Nº 28  Emilia Toro de Balmaceda.

 

                    El 26 de noviembre  de 1986, el Ministerio entrega la administración del establecimiento, mediante el Decreto ley 3166,  a la Corporación de Educación Popular. En la fundamentación del decreto se dice que “para el mejoramiento de la enseñanza, el Liceo se vincule con el sector productivo de modo de preparar Técnicos de Nivel Medio”.

 

El 3 de diciembre de 1993,  el MINEDUC entrega la administración de nuestro colegio  a la Universidad Metropolitana de Ciencias de la Educación.

 

El 1 de marzo de 1997, asume la administración del Liceo la Universidad Tecnológica Metropolitana.

 

Por resolución Nº 2268 del 29 de julio de 2001, y en consideración a las cinco especialidades impartidas, adquiere la categoría de Polivalente. Estas especialidades son: Atención de Párvulos, Atención al Adulto Mayor, Alimentación Colectiva, Administración, Vestuario y Confección Textil.

El día 7 de julio del año 2006 el Liceo en pleno, con todos sus estamentos, celebró solemnemente un siglo de existencia.

 

El año 2009  llega a su fin la especialidad de Vestuario y confección Textil, que había sido desde la fundación la especialidad  más emblemática y distintiva del Colegio. En diciembre de ese año se licencia el último 4º año Medio de la especialidad.

 

Desde el año 2005 a  la fecha el Equipo Directivo lo conforman: Directora   Señora Gladys del Carmen Zúñiga Contreras, Inspectora General  Señora María Loreto Muñiz Marín, Jefa de la Unidad Técnica Pedagógica Sra. Ana María Peralta Arenas. Actualmente se imparten las especialidades de: Atención de Párvulos, Atención de Enfermería mención Adulto Mayor, Gastronomía mención Cocina y Administración mención Recursos Humanos.</p>
